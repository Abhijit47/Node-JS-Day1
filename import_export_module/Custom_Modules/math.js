const additionNumber = (num1, num2) => {
  return num1 + num2;
};

const substractNumber = (num1, num2) => {
  return num1 - num2;
};

const multiplyNumber = (num1, num2) => {
  return num1 * num2;
};

const divideNumber = (num1, num2) => {
  return num1 / num2;
};

module.exports = {
  additionNumber,
  substractNumber,
  multiplyNumber,
  divideNumber
}

